# !----------------> import sys <----------------!   # https://docs.python.org/2.7/library/sys.html
sys.version                             # Python sürümünü ver derleyici hakkında ek bilgi döner.            (Return Type String)
sys.copyright                           # Python hakkında telif hakkını verir.                              (Return Type String)
sys.executable                          # Pyhton ikili dosyasının pathni verir.                             (Return Type String)
sys.builtin_module_names                # Pythonda derlenen tüm modüllerin isimlerini tuple olarak verir.   (Return Type Tuple)
sys.modules                             # Pythonda ki modül dictionary verir.                               (Return Type Dictionary)
sys.platform                            # Hangi platformda çalışıyor ise onu döner.                         (Return Type String)
sys.stdin                               # Standart input        (Return Type _io.TextIOWrapper)
sys.stdout                              # Standart output       (Return Type _io.TextIOWrapper)
sys.stderr                              # Standart error        (Return Type _io.TextIOWrapper)
sys.argv                                # Command line Arguments (Return Type List)
sys.getwindowsversion()                 # Windows sürümünü verir.
sys.exit([args])                        # Python exit

