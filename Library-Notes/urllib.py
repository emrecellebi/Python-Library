# !----------------> import urllib <----------------! # https://docs.python.org/2.7/library/urllib.html
urllib.urlopen()                        # Belirtilen URL yi açar
urllib.close()                          # Açılan URL yi kapatır
urllib.read()                           # Content içeriğini okur.
urllib.getcode()                        # Status code döner.
urllib.geturl()                         # İstek atılan linki verir.
urllib.url                              # İstek atılan linki verir.
urllib.urlretrieve()                    # Verilen URL bir resim dosyası olarak alır.
urllib.urlcleanup()                     # Önbelliği temizler
urllib.quote()                          # > vb özel karakterleri değiştirir.
urllib.unquote()                        # %2C vb özel karakterleri değiştirir.
